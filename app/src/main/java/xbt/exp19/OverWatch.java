public class OverWatch {

    private String name;

    private int imageID;

    public OverWatch(String name, int imageID) {
        this.imageID = imageID;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getImageID() {
        return imageID;
    }
}
